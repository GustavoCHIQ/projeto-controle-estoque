    private void entrar() {
        modelUsuario.setUsuLogin(jtfLogin.getText());
        modelUsuario.setUsuSenha(String.valueOf(jtfSenha.getPassword()));
        if (CONTROLLER_USUARIO.validarUsuarioController(modelUsuario)) {
            modelUsuario = CONTROLLER_USUARIO.getUsuarioController(jtfLogin.getText());
            modelSessaoUsuario.codigo = modelUsuario.getUsuId();
            modelSessaoUsuario.nome = modelUsuario.getUsuNome();
            modelSessaoUsuario.login = modelUsuario.getUsuLogin();
            new ViewPrincipal().setVisible(true);
            dispose();
        } else {
            JOptionPane.showMessageDialog(this, Mensagens.getMessage("LGN_SEM_USUARIO"), Mensagens.getMessage("ATENCAO"), JOptionPane.WARNING_MESSAGE);
        }
    }

